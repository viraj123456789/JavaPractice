// row = 1-4 col=1-4

public class Numbers_1
{

	public static void main(String[] args) 
	{
		for (int row=1; row<=4; row++)
		{
			for(int col=1 ; col<=4 ; col++)
			{
				System.out.print(row);	
			}
			System.out.println();
		}
		
	}

}
